void draw_point(void)
{
	for(uint16_t y=0; y<30; y++)
	{
		for(uint16_t x=0; x<40; x++)
		{
			if(pacmanMap[y][x]==2) 		FillRect(x*8+3, y*8+3, 2, 2, clPoint);
			else if(pacmanMap[y][x]==3)	FillRect(x*8+2, y*8+2, 4, 4, clPoint);
			else if(pacmanMap[y][x]==0)	FillRect(x*8+3, y*8+3, 2, 2, clBlack);
		}
	}
}

void sprite_redraw(SPRITE *sprite, uint16_t x, uint16_t y, uint8_t face_dir)
{
	int8_t pdy=-4;
	int8_t pdx=-4;
	int8_t gd = 0;
	uint8_t type = sprite->type;
	switch(type)
	{
	//------PACMAN DRAW-----------
		case PACMAN:
			switch(face_dir)
			{
				case UP:
					draw_Bitmap(x*8+pdx,y*8+pdy, pacman_u, sprite->main_color, clBlack, 16, 16);
				break;

				case DOWN:
					draw_Bitmap(x*8+pdx,y*8+pdy, pacman_d, sprite->main_color, clBlack, 16, 16);
				break;

				case RIGHT:
					draw_Bitmap(x*8+pdx,y*8+pdy, pacman_r, sprite->main_color, clBlack, 16, 16);
				break;

				case LEFT:
					draw_Bitmap(x*8+pdx,y*8+pdy, pacman_l, sprite->main_color, clBlack, 16, 16);
				break;

				case CNT:
					draw_Bitmap(x*8+pdx,y*8+pdy, pacman_o, sprite->main_color, clBlack, 16, 16);
				break;
			}
		break;

	//------GHOST DRAW-----------
		case GHOST:
			draw_Bitmap(x*8+gd,y*8+gd, ghost_mask, sprite->main_color, clBlack, 16, 16);	//Ghost frame

			//Left eye
			FillRect(x*8+3+gd, y*8+4+gd, 2, 5, clGray2);
			FillRect(x*8+2+gd, y*8+5+gd, 4, 3, clGray2);

			//Left eye
			FillRect(x*8+9+gd, y*8+4+gd, 2, 5, clGray2);
			FillRect(x*8+8+gd, y*8+5+gd, 4, 3, clGray2);

			switch(sprite->face_dir)
			{
				case CNT: 	FillRect(x*8+3+gd, y*8+6+gd, 2, 2, clBlue); FillRect(x*8+9+gd, y*8+6+gd, 2, 2, clBlue); 	break;
				case UP: 	FillRect(x*8+3+gd, y*8+5+gd, 2, 2, clBlue);	FillRect(x*8+9+gd, y*8+5+gd, 2, 2, clBlue); 	break;
				case DOWN:	FillRect(x*8+3+gd, y*8+7+gd, 2, 2, clBlue);	FillRect(x*8+9+gd, y*8+7+gd, 2, 2, clBlue);		break;
				case RIGHT:	FillRect(x*8+4+gd, y*8+6+gd, 2, 2, clBlue);	FillRect(x*8+10+gd, y*8+6+gd, 2, 2, clBlue);	break;
				case LEFT:	FillRect(x*8+2+gd, y*8+6+gd, 2, 2, clBlue);	FillRect(x*8+8+gd, y*8+6+gd, 2, 2, clBlue);		break;
			}
		break;
	}
	if(pacmanMap[Pacman.y][Pacman.x] == 2) { pacmanMap[Pacman.y][Pacman.x]=0; point+=10; }
	draw_point();
}

